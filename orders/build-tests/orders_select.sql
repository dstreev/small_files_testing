    ORDER_ID
  , USER_ID
  , COUNT(PRODUCT_ID)
  , SUM(QUANTITY * COST)
FROM
    ORDER_SMALL_${FILE_TYPE}
        JOIN ORDER_ITEM_SMALL_${FILE_TYPE}
            ON ORDER_SMALL_${FILE_TYPE}.ID = ORDER_ITEM_SMALL_${FILE_TYPE}.ORDER_ID
WHERE
    QUANTITY > 5
GROUP BY
    ORDER_ID, USER_ID
LIMIT 10;
SELECT 'END SMALL-FILE ORDER/ORDER_ITEM QUERY', `current_timestamp`();
